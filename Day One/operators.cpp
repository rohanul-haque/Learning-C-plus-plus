#include <iostream>

using namespace std;

int main()
{

    int x = 10;
    int y = 3;

    cout << x + y << endl;
    cout << x - y << endl;
    cout << x * y << endl;
    cout << x / y << endl;
    cout << x % y << endl;

    int z = 10;

    ++z;

    cout << z << endl;

    --z;

    cout << z << endl;

    return 0;
}
